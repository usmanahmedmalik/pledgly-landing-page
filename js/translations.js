document.addEventListener('alpine:init', () => {
    Alpine.data('app', () => ({
        currentLang: 'nl',
        currentTheme: localStorage.getItem('pledgly_theme') || 'light',
        translations: translations,

        init() {
            const savedLang = localStorage.getItem('pledgly_lang');
            if (savedLang && ['en', 'nl', 'de', 'fr', 'ar'].includes(savedLang)) {
                this.currentLang = savedLang;
            }

            // Theme initialization
            this.currentTheme = localStorage.getItem('pledgly_theme') || 'light';

            this.updateDir();
            this.updateTheme();
        },

        toggleLanguage(lang) {
            this.currentLang = lang;
            localStorage.setItem('pledgly_lang', lang);
            this.updateDir();
        },

        toggleTheme() {
            this.currentTheme = this.currentTheme === 'dark' ? 'light' : 'dark';
            localStorage.setItem('pledgly_theme', this.currentTheme);
            this.updateTheme();
        },

        updateTheme() {
            if (this.currentTheme === 'dark') {
                document.documentElement.classList.add('dark');
            } else {
                document.documentElement.classList.remove('dark');
            }
        },

        updateDir() {
            document.dir = this.currentLang === 'ar' ? 'rtl' : 'ltr';
        },

        t(key) {
            const keys = key.split('.');
            let output = this.translations;
            for (let k of keys) {
                if (output && output[k]) {
                    output = output[k];
                } else {
                    return key; // Fallback to key if not found
                }
            }
            return output[this.currentLang] || output['en'] || key;
        },

        // For arrays/lists or special structures if needed
        isRtl() {
            return this.currentLang === 'ar';
        }
    }));
});
